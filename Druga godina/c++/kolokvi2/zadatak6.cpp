/*
Stog (stack):
- Linearna struktura podataka
- Elementi se dodaju i uklanjaju samo s jednog kraja (vrha)

Osnovna svojstva stoga:

Dodavanje elementa na vrh (push)
Uklanjanje elementa s vrha (pop)
Pristup elementu na vrhu bez uklanjanja (top/peek)
Provjera je li stog prazan
LIFO:

Znači "Last In, First Out" (Zadnji unutra, prvi van)
Opisuje princip rada stoga
Element koji je zadnji dodan, prvi se uklanja
*/

/*
Stog (stack) je linearna struktura podataka koja slijedi princip LIFO (Last In, First Out). Ovaj princip znači da se element koji je posljednji dodan na stog prvi uklanja. Stog se može zamisliti kao vertikalni niz elemenata gdje se operacije izvode samo na jednom kraju, koji se naziva vrh stoga.

Osnovna svojstva stoga uključuju:

Push operaciju, koja dodaje element na vrh stoga.
Pop operaciju, koja uklanja element s vrha stoga.
Top (ili peek) operaciju, koja omogućuje pregled elementa na vrhu stoga bez njegovog uklanjanja.
Mogućnost provjere je li stog prazan.
LIFO princip je ključno svojstvo stoga koje ga razlikuje od drugih struktura podataka. Ovaj princip osigurava da je element koji je posljednji dodan na stog uvijek prvi dostupan za uklanjanje.
*/